//Primitive Types
// 1. Number
let num = 100000;
console.log(num);
//can add negative
num = -1000;
console.log(num);
//floating
num = 20.1;
console.log(num);
//gets the type of the variable
console.log(typeof num);

//Basic Math
let num1 = 2;
let num2 = 5;

console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);
console.log(num1 % num2);
console.log(num1 ** num2); //power

//increment/decrement

let counter = 0;
counter++;
console.log(counter);
counter--;
console.log(counter);
