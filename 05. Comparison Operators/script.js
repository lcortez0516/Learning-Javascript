//Relational Operators

console.log(10 > 10); //true or false
console.log(10 < 50); //less than
console.log(10 >= 10); // greater than or equal
console.log(10 <= 10); // lessthan or equal

//Equality Operators
// === strict equality (type + value)
// !==  strict non-equality (type + value)
// == lose equality operator (checks the value)
// != lose not equality operator (value)

console.log(10 === 10); //true
console.log("10" === 10); //false
console.log("10" == 10); //true
console.log("10" != 10); //false
console.log("10" !== 10); //True
